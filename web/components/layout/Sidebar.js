export default function Sidebar({ counts = {} }) {
  const pathname = usePathname();
  const router = useRouter();
  const [searchQuery, setSearchQuery] = useState("");

  useEffect(() => {
    function onKeyDown(e) {
      if (e.key === "/" && !["INPUT", "TEXTAREA"].includes(document.activeElement?.tagName)) {
        e.preventDefault();
        document.getElementById("sidebar-search")?.focus();
      }
    }
    window.addEventListener("keydown", onKeyDown);
    return () => window.removeEventListener("keydown", onKeyDown);
  }, []);

  function handleSearch(e) {
    e.preventDefault();
    if (searchQuery.trim()) {
      router.push(`/search?q=${encodeURIComponent(searchQuery.trim())}`);
    }
  }

  function isActive(href) {
    if (href === "/") return pathname === "/";
    return pathname.startsWith(href);
  }

  return (
    <aside className="fixed left-0 top-0 bottom-0 w-56 bg-dd-sidebar flex flex-col z-40">
      {/* Logo */}
      <div className="px-4 pt-5 pb-4">
        <div className="flex items-center gap-3">
          <div className="w-8 h-8 rounded-lg bg-gradient-to-br from-dd-green to-dd-teal flex items-center justify-center flex-shrink-0">
            <svg className="w-4.5 h-4.5 text-white" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
              <path strokeLinecap="round" strokeLinejoin="round" d="M21 21l-5.197-5.197m0 0A7.5 7.5 0 105.196 5.196a7.5 7.5 0 0010.607 10.607z" />
            </svg>
          </div>
          <div>
            <div className="text-white font-semibold text-sm">PSE Hub</div>
            <div className="text-white/50 text-xs">Customer Support</div>
          </div>
        </div>
      </div>

      {/* Search */}
      <div className="px-3 pb-4">
        <form onSubmit={handleSearch}>
          <div className="relative">
            <input
              id="sidebar-search"
              type="text"
              placeholder="Search... (/)"
              value={searchQuery}
              onChange={(e) => setSearchQuery(e.target.value)}
              className="w-full bg-white/10 text-white placeholder-white/40 text-sm rounded-lg
                         px-3 py-2 pr-8 border border-white/10 focus:border-dd-purple-light
                         focus:outline-none focus:ring-1 focus:ring-dd-purple-light"
            />
            <svg className="w-4 h-4 text-white/40 absolute right-2.5 top-2.5" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
              <path strokeLinecap="round" strokeLinejoin="round" d="M21 21l-5.197-5.197m0 0A7.5 7.5 0 105.196 5.196a7.5 7.5 0 0010.607 10.607z" />
            </svg>
          </div>
        </form>
      </div>

      {/* Navigation */}
      <nav className="flex-1 px-3 space-y-0.5 overflow-y-auto">
        {NAV_ITEMS.map((item, idx) => {
          if (item.separator) {
            return <div key={`sep-${idx}`} className="my-2 border-t border-white/10" />;
          }
          const active = isActive(item.href);
          const count = item.countKey ? counts[item.countKey] : null;
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex items-center gap-3 px-3 py-2 rounded-lg text-sm transition-colors
                ${active
                  ? "bg-dd-purple text-white font-medium"
                  : "text-white/70 hover:text-white hover:bg-white/10"
                }`}
            >
              {item.icon}
              <span className="flex-1">{item.label}</span>
              {count != null && count > 0 && (
                <span className={`text-xs px-1.5 py-0.5 rounded-full ${
                  active ? "bg-white/20" : "bg-white/10"
                }`}>
                  {count}
                </span>
              )}
            </Link>
          );
        })}
      </nav>

      {/* Footer */}
      <div className="px-3 py-3 border-t border-white/10">
        <div className="flex items-center justify-between">
          <div className="flex items-center gap-2 text-xs text-white/50">
            <span className="w-2 h-2 rounded-full bg-dd-green" />
            Local
          </div>
          <ThemeToggle />
        </div>
      </div>
    </aside>
  );
}
